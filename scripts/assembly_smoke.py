"""Recurring rung-2 assembly smoke check for the Phase 4 bulk conversion run.

Compile ALL currently emitted Poryscript output (.pory → .inc), drop them into a
throwaway git worktree of the pokeemerald-expansion fork together with generated
support headers, run ``make modern``, and cluster the assembly errors (duplicate
symbols, undefined references).

The per-event compile gate cannot see assembly-class bugs that span multiple
compiled files: duplicate labels across maps, constants that compile clean in
isolation but are unresolvable at link/assemble time.  This script catches them.
Run it between bulk rounds while the machine idles on the 5-hour pause window.

Design rationale: FABLES_DECISIONS.md "Suggestion 4 — Recurring rung-2 assembly
smoke check".  Proven manual wiring: reference/memory-archive.md "Rung-2
derisking spike" (branch rung2-map200-spike, 2026-05-31).

Usage (most common):
  python scripts/assembly_smoke.py               # prepare → build → report
  python scripts/assembly_smoke.py --prepare-only  # stage only; build later

Subcommands:
  prepare   Compile .pory→.inc, generate support headers, wire includes
  build     Run make -j<nproc> modern in the smoke worktree (takes minutes)
  report    Parse smoke-build.log and print error clusters
  teardown  Remove the worktree and delete the throwaway branch
"""
from __future__ import annotations

import argparse
import json
import logging
import multiprocessing
import os
import re
import subprocess
import sys
from pathlib import Path

from rpg2gba import pipeline

logger = logging.getLogger("assembly_smoke")

# ---------------------------------------------------------------------------
# Repository-layout constants
# ---------------------------------------------------------------------------

_REPO_ROOT = Path(__file__).resolve().parents[1]
_SMOKE_WORKTREE = _REPO_ROOT / "output" / "smoke-worktree"
_SMOKE_BRANCH = "rpg2gba-smoke-branch"
_BUILD_LOG = _REPO_ROOT / "output" / "smoke-build.log"

# Relative to the smoke worktree
_SMOKE_INC_SUBDIR = Path("data") / "scripts" / "rpg2gba_smoke"

# Idempotency markers for the two wiring blocks in event_scripts.s
_HDR_BEGIN = "@ rpg2gba-smoke-headers begin"
_HDR_END = "@ rpg2gba-smoke-headers end"
_SCR_BEGIN = "@ rpg2gba-smoke-scripts begin"
_SCR_END = "@ rpg2gba-smoke-scripts end"

# Line in event_scripts.s before which we insert the constants-header block.
# This line sits at the natural boundary between the #include section and the
# .include / .section assembler directives (verified in rung-2 spike notes).
_HDR_ANCHOR = '.include "asm/macros.inc"'

# ---------------------------------------------------------------------------
# Pure helpers — no git, no make, no binary (fully unit-testable)
# ---------------------------------------------------------------------------

_MAP_TOKEN_RE = re.compile(r"\bMAP_URANIUM_(\d+)\b")
_TRAINER_TOKEN_RE = re.compile(r"\bTRAINER_[A-Z0-9_]+\b")
_ALLCAPS_RE = re.compile(r"\b([A-Z][A-Z0-9_]{2,})\b")


def scan_map_tokens(text: str) -> set[str]:
    """Return all distinct MAP_URANIUM_<N> token names referenced in *text*."""
    return {m.group(0) for m in _MAP_TOKEN_RE.finditer(text)}


def emit_map_aliases_header(tokens: set[str], target_map_const: str) -> str:
    """Emit a C header that #define-aliases every MAP_URANIUM_<N> token.

    Each alias points to *target_map_const* (the first MAP_* defined in the
    fork's include/constants/map_groups.h, e.g. MAP_PETALBURG_CITY).

    Why this works with the warp macro: the ``warp``/``formatwarp`` GAS macros
    call ``.ifdef \\map_id`` on the map argument.  The C preprocessor runs BEFORE
    the assembler; by the time the assembler sees the code, ``MAP_URANIUM_N`` has
    already been textually substituted to ``MAP_PETALBURG_CITY``.  The ``.ifdef``
    then tests the real assembler symbol, which passes.  Verified by reading
    ``asm/macros/map.inc``::``map`` macro (lines 4–11: ``.ifdef \\map_id; .byte
    \\map_id >> 8; .byte \\map_id & 0xFF; .else; .error ...; .endif``).
    """
    lines = [
        "#ifndef GUARD_RPG2GBA_MAP_ALIASES_H",
        "#define GUARD_RPG2GBA_MAP_ALIASES_H",
        "",
        "// Generated by scripts/assembly_smoke.py — do not hand-edit.",
        "// Placeholder MAP_URANIUM_<N> aliases for smoke assembly.",
        "// Every alias resolves to the fork's first MAP_* constant, a plain integer",
        "// expression.  The C preprocessor substitutes these before the assembler",
        "// sees them, so the warp macro's .ifdef check passes correctly.",
        f"// Verified alias target: {target_map_const}",
        "",
    ]
    for token in sorted(tokens, key=lambda t: int(t.split("_")[-1])):
        lines.append(f"#define {token} {target_map_const}")
    lines += ["", "#endif  // GUARD_RPG2GBA_MAP_ALIASES_H", ""]
    return "\n".join(lines)


def scan_trainer_tokens(text: str) -> set[str]:
    """Return all distinct TRAINER_[A-Z0-9_]+ token names referenced in *text*."""
    return set(_TRAINER_TOKEN_RE.findall(text))


def emit_trainer_aliases_header(
    tokens: set[str],
    trainer_ids: dict[str, int],
    fork_trainer_consts: set[str],
) -> str:
    """Emit a C header defining TRAINER_* integer ids from trainers.json.

    Args:
        tokens: every TRAINER_* token found in compiled .inc output.
        trainer_ids: mapping ``TRAINER_CONSTANT -> integer_id`` from
            ``output/uranium-build/intermediate/trainers.json``.
        fork_trainer_consts: TRAINER_* names already defined in the fork's
            ``include/constants/opponents.h`` and related headers — skipped to
            avoid redefinition errors.

    Tokens that appear in neither ``fork_trainer_consts`` nor ``trainer_ids``
    are left undefined; the fail-loud ``find_unresolved_families`` check catches
    them after all three headers have been generated.
    """
    lines = [
        "#ifndef GUARD_RPG2GBA_TRAINER_ALIASES_H",
        "#define GUARD_RPG2GBA_TRAINER_ALIASES_H",
        "",
        "// Generated by scripts/assembly_smoke.py — do not hand-edit.",
        "// Uranium TRAINER_* constants from output/uranium-build/intermediate/trainers.json.",
        "// Only constants referenced in compiled output that are absent from opponents.h",
        "// are emitted here.",
        "",
    ]
    defined_any = False
    for token in sorted(tokens):
        if token in fork_trainer_consts:
            continue  # already defined in the fork — do not redefine
        if token in trainer_ids:
            lines.append(f"#define {token} {trainer_ids[token]}")
            defined_any = True
        # tokens in neither: silently omitted; fail-loud check handles them
    if not defined_any:
        lines.append("// (no Uranium-specific TRAINER_* constants in current compiled output)")
    lines += ["", "#endif  // GUARD_RPG2GBA_TRAINER_ALIASES_H", ""]
    return "\n".join(lines)


def find_unresolved_families(
    text: str,
    all_known: set[str],
) -> dict[str, list[str]]:
    """Scan compiled .inc text for ALL_CAPS tokens not in *all_known*.

    Groups unknown tokens by their prefix family (the first word before ``_``).
    Returns ``{family: [example_token, ...]}``.  An empty dict means every
    referenced constant resolves against the fork headers or a generated header.

    Only considers tokens that contain at least one underscore — bare ALL_CAPS
    words (``PUSH``, ``LDR``, ``BL``) are GAS/ARM mnemonics, not constant names.
    """
    unresolved: dict[str, list[str]] = {}
    seen: set[str] = set()
    for token in _ALLCAPS_RE.findall(text):
        if token in all_known or token in seen:
            continue
        seen.add(token)
        if "_" not in token:
            continue  # GAS mnemonic or bare short word — skip
        family = token.split("_")[0]
        unresolved.setdefault(family, []).append(token)
    return unresolved


def cluster_build_errors(log_text: str) -> dict[str, object]:
    r"""Parse a make build log into error clusters.

    Recognises three error classes:

    * **Duplicate symbol definitions** — GAS assembler:
      ``Error: symbol \`LABEL' is already defined``
    * **Undefined references** — GNU ld:
      ``undefined reference to \`SYMBOL'``
    * **Other errors** — any line containing ``Error:`` or ``error:``
      not matched by the above patterns.

    Returns::

        {
            "duplicate_symbols": list[str],           # deduped symbol names
            "undefined_by_family": dict[str, list[str]],  # prefix → names
            "other_errors": list[str],                # raw error text lines
        }
    """
    _DUP_RE = re.compile(r"symbol `([^']+)' is already defined")
    _UNDEF_RE = re.compile(r"undefined reference to `([^']+)'")

    dup_symbols: list[str] = []
    dup_seen: set[str] = set()
    undef_refs: list[str] = []
    undef_seen: set[str] = set()
    other_errors: list[str] = []
    other_seen: set[str] = set()

    for line in log_text.splitlines():
        dm = _DUP_RE.search(line)
        if dm:
            sym = dm.group(1)
            if sym not in dup_seen:
                dup_symbols.append(sym)
                dup_seen.add(sym)
            continue

        um = _UNDEF_RE.search(line)
        if um:
            sym = um.group(1)
            if sym not in undef_seen:
                undef_refs.append(sym)
                undef_seen.add(sym)
            continue

        stripped = line.strip()
        if stripped and ("Error:" in stripped or "error:" in stripped):
            if stripped not in other_seen:
                other_errors.append(stripped)
                other_seen.add(stripped)

    # Group undefined refs by prefix family (first word before "_")
    undef_by_family: dict[str, list[str]] = {}
    for sym in undef_refs:
        family = sym.split("_")[0] if "_" in sym else sym
        undef_by_family.setdefault(family, []).append(sym)

    return {
        "duplicate_symbols": dup_symbols,
        "undefined_by_family": undef_by_family,
        "other_errors": other_errors,
    }


def wire_includes(
    source: str,
    header_lines: list[str],
    script_lines: list[str],
) -> str:
    """Return event_scripts.s text with rpg2gba smoke includes wired in.

    Idempotent: uses marker comments (``@ rpg2gba-smoke-headers begin/end`` and
    ``@ rpg2gba-smoke-scripts begin/end``) to bracket the inserted blocks.
    Running twice with identical arguments produces identical output; running
    with updated arguments replaces the old block wholesale.

    Header ``#include`` lines are inserted just before the
    ``'.include "asm/macros.inc"'`` anchor (the natural boundary between the C
    preprocessor ``#include`` section and the GAS ``.include`` section in
    event_scripts.s, confirmed in the rung-2 spike narrative).  Script
    ``.include`` lines are appended at the end of the file.
    """
    header_block = "\n".join([_HDR_BEGIN] + header_lines + [_HDR_END])
    scripts_block = "\n".join([_SCR_BEGIN] + script_lines + [_SCR_END])

    # -- headers block --------------------------------------------------------
    if _HDR_BEGIN in source and _HDR_END in source:
        # Replace existing block wholesale (idempotent update)
        pat = re.compile(
            re.escape(_HDR_BEGIN) + r".*?" + re.escape(_HDR_END), re.DOTALL
        )
        source = pat.sub(header_block, source, count=1)
    else:
        # First insertion: add before the anchor line (preserving its indentation)
        src_lines = source.splitlines(keepends=True)
        inserted = False
        for i, line in enumerate(src_lines):
            if _HDR_ANCHOR in line:
                src_lines.insert(i, header_block + "\n")
                inserted = True
                break
        source = "".join(src_lines) if inserted else header_block + "\n" + source

    # -- scripts block --------------------------------------------------------
    if _SCR_BEGIN in source and _SCR_END in source:
        pat = re.compile(
            re.escape(_SCR_BEGIN) + r".*?" + re.escape(_SCR_END), re.DOTALL
        )
        source = pat.sub(scripts_block, source, count=1)
    else:
        source = source.rstrip("\n") + "\n" + scripts_block + "\n"

    return source


