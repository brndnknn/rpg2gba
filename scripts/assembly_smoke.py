def cmd_prepare(fork_path: Path, out_dir: Path, define_unresolved: bool = False) -> int:
    """Compile .pory→.inc, generate support headers, wire includes idempotently."""
    scripts_dir = out_dir / "scripts"
    pory_files = sorted(scripts_dir.glob("*.pory"))
    if not pory_files:
        logger.error("no .pory files found in %s — run phase4 first", scripts_dir)
        return 1

    # 1. Create or reuse the throwaway worktree
    _create_or_reuse_worktree(fork_path, _SMOKE_WORKTREE, _SMOKE_BRANCH)

    # 2. Compile every .pory → .inc
    smoke_inc_dir = _SMOKE_WORKTREE / _SMOKE_INC_SUBDIR
    smoke_inc_dir.mkdir(parents=True, exist_ok=True)

    compile_failures: list[str] = []
    compiled_inc_files: list[Path] = []

    for pory_file in pory_files:
        inc_path = smoke_inc_dir / (pory_file.stem + ".inc")
        try:
            ok, stderr = _compile_pory_to_inc(pory_file, inc_path)
        except RuntimeError as exc:
            logger.error("poryscript unavailable — cannot compile .pory files: %s", exc)
            return 1
        if not ok:
            logger.warning(
                "transpile failure (finding): %s — %s",
                pory_file.name,
                stderr.strip()[:300],
            )
            compile_failures.append(pory_file.stem)
            continue
        compiled_inc_files.append(inc_path)

    if compile_failures:
        logger.warning(
            "%d transpile failure(s) — skipped, recorded as findings: %s",
            len(compile_failures),
            ", ".join(compile_failures),
        )

    if not compiled_inc_files:
        logger.error("all .pory files failed to transpile — nothing to assemble")
        return 1

    all_inc_text = "\n".join(
        f.read_text(encoding="utf-8") for f in compiled_inc_files
    )

    # 3a. Flag/var header from the live registry (read-only, clean=False)
    registry = pipeline._phase4_registry(out_dir, clean=False, fork_path=fork_path)
    flags_h = _SMOKE_WORKTREE / "include" / "constants" / "rpg2gba_flags.h"
    registry.dump_header(flags_h)

    # 3b. Map aliases header
    map_tokens = scan_map_tokens(all_inc_text)
    logger.info("MAP_URANIUM_* tokens in compiled output: %d", len(map_tokens))
    target_map_const = _get_first_map_const(fork_path)
    logger.info("alias target constant: %s", target_map_const)
    map_aliases_h = _SMOKE_WORKTREE / "include" / "constants" / "rpg2gba_map_aliases.h"
    map_aliases_h.parent.mkdir(parents=True, exist_ok=True)
    map_aliases_h.write_text(
        emit_map_aliases_header(map_tokens, target_map_const),
        encoding="utf-8",
    )

    # 3c. Trainer aliases header
    trainer_tokens = scan_trainer_tokens(all_inc_text)
    fork_trainer_consts = _load_fork_trainer_consts(fork_path)
    try:
        trainers_id_map = _load_trainers_id_map(out_dir)
    except FileNotFoundError as exc:
        logger.warning("trainers.json unavailable — TRAINER_* aliases omitted: %s", exc)
        trainers_id_map = {}
    trainer_h = _SMOKE_WORKTREE / "include" / "constants" / "rpg2gba_trainer_aliases.h"
    trainer_h.parent.mkdir(parents=True, exist_ok=True)
    trainer_h.write_text(
        emit_trainer_aliases_header(trainer_tokens, trainers_id_map, fork_trainer_consts),
        encoding="utf-8",
    )

    # 3d. Fail-loud check: any constant family still unresolved is a genuine bug
    all_known: set[str] = _load_all_fork_constants(fork_path)
    for h in (flags_h, map_aliases_h, trainer_h):
        all_known |= _read_header_constants(h)
    unresolved = find_unresolved_families(all_inc_text, all_known)
    unresolved_h = _SMOKE_WORKTREE / "include" / "constants" / "rpg2gba_smoke_unresolved.h"
    if unresolved and not define_unresolved:
        lines = [
            "ERROR: unresolved constant families in compiled output (CLAUDE.md §4.5).",
            "These families are referenced in assembled scripts but not defined by",
            "any fork header or generated support header.  Add a header for each family,",
            "fix the converter that emitted these tokens, or — once each family is",
            "recorded as a finding — re-run with --define-unresolved to dummy-define",
            "them (smoke worktree only) so the build can proceed.",
            "",
        ]
        for fam, syms in sorted(unresolved.items()):
            examples = syms[:3]
            lines.append(f"  {fam}:  {len(syms)} token(s) — e.g. {examples}")
        print("\n".join(lines), file=sys.stderr)
        return 1
    if unresolved:
        # Opt-in: dummy-define every unresolved token to 0 in a smoke-only header.
        # The FINDING is the unresolved name itself (recorded loudly below); the
        # dummy value exists solely so assembly reaches the duplicate-symbol /
        # undefined-reference checks this harness is for.
        hdr = [
            "#ifndef GUARD_RPG2GBA_SMOKE_UNRESOLVED_H",
            "#define GUARD_RPG2GBA_SMOKE_UNRESOLVED_H",
            "",
            "// Generated by scripts/assembly_smoke.py --define-unresolved — do not hand-edit.",
            "// SMOKE-ONLY dummies for constants with no real definition. Each name here",
            "// is a recorded conversion finding, NOT a resolved constant.",
            "",
        ]
        for fam, syms in sorted(unresolved.items()):
            logger.warning(
                "FINDING — unresolved %s family: %d token(s): %s",
                fam, len(syms), ", ".join(sorted(syms)),
            )
            for sym in sorted(syms):
                hdr.append(f"#define {sym} 0")
        hdr += ["", "#endif  // GUARD_RPG2GBA_SMOKE_UNRESOLVED_H", ""]
        unresolved_h.write_text("\n".join(hdr), encoding="utf-8")
    elif unresolved_h.exists():
        unresolved_h.unlink()  # stale dummies from a prior run must not mask fixes

    # 4. Wire includes into the worktree's event_scripts.s (idempotent)
    event_scripts_s = _SMOKE_WORKTREE / "data" / "event_scripts.s"
    source = event_scripts_s.read_text(encoding="utf-8")

    header_includes = [
        '#include "constants/rpg2gba_flags.h"',
        '#include "constants/rpg2gba_map_aliases.h"',
        '#include "constants/rpg2gba_trainer_aliases.h"',
    ]
    if unresolved_h.exists():
        header_includes.append('#include "constants/rpg2gba_smoke_unresolved.h"')
    script_includes = [
        f'.include "{_SMOKE_INC_SUBDIR.as_posix()}/{inc.name}"'
        for inc in sorted(compiled_inc_files, key=lambda p: p.name)
    ]

    new_source = wire_includes(source, header_includes, script_includes)
    event_scripts_s.write_text(new_source, encoding="utf-8")

    logger.info(
        "prepare complete: %d .inc files compiled, %d transpile failure(s); "
        "worktree: %s",
        len(compiled_inc_files),
        len(compile_failures),
        _SMOKE_WORKTREE,
    )
    return 0


def cmd_build(out_dir: Path) -> int:
    """Run ``make -j<nproc> modern`` in the smoke worktree; capture to smoke-build.log."""
    if not _SMOKE_WORKTREE.is_dir():
        logger.error("worktree not found at %s — run `prepare` first", _SMOKE_WORKTREE)
        return 1

    nproc = multiprocessing.cpu_count()
    _BUILD_LOG.parent.mkdir(parents=True, exist_ok=True)

    print(f"Running make -j{nproc} modern in {_SMOKE_WORKTREE}", flush=True)
    print(
        "Note: the FIRST build is a full ROM compile and takes several minutes.\n"
        "Subsequent incremental builds (unchanged sources) are much faster.",
        flush=True,
    )

    proc = subprocess.run(
        ["make", f"-j{nproc}", "modern"],
        cwd=str(_SMOKE_WORKTREE),
        capture_output=True,
        text=True,
    )
    combined = proc.stdout + "\n" + proc.stderr
    _BUILD_LOG.write_text(combined, encoding="utf-8")

    if proc.returncode == 0:
        print(f"Build SUCCEEDED.  Log: {_BUILD_LOG}")
    else:
        print(f"Build FAILED (exit {proc.returncode}).  Log: {_BUILD_LOG}")
    return proc.returncode


def cmd_report() -> int:
    """Parse smoke-build.log and print a clustered error summary."""
    if not _BUILD_LOG.is_file():
        logger.error("build log not found at %s — run `build` first", _BUILD_LOG)
        return 1

    log_text = _BUILD_LOG.read_text(encoding="utf-8")
    clusters = cluster_build_errors(log_text)

    dup_syms: list[str] = clusters["duplicate_symbols"]  # type: ignore[assignment]
    undef_by_fam: dict[str, list[str]] = clusters["undefined_by_family"]  # type: ignore[assignment]
    other: list[str] = clusters["other_errors"]  # type: ignore[assignment]

    total_undef = sum(len(v) for v in undef_by_fam.values())
    total = len(dup_syms) + total_undef + len(other)

    print("\n=== Assembly Smoke Report ===")
    print(f"Log: {_BUILD_LOG}\n")

    print(f"Duplicate symbols: {len(dup_syms)}")
    for sym in dup_syms[:3]:
        print(f"  {sym}")
    if len(dup_syms) > 3:
        print(f"  ... and {len(dup_syms) - 3} more")

    print(f"\nUndefined references: {total_undef} total across {len(undef_by_fam)} families")
    for fam, syms in sorted(undef_by_fam.items()):
        extras = f" (+{len(syms) - 3} more)" if len(syms) > 3 else ""
        print(f"  {fam:20s}  {len(syms):4d}  e.g. {syms[:3]}{extras}")

    print(f"\nOther errors: {len(other)}")
    for line in other[:3]:
        print(f"  {line}")
    if len(other) > 3:
        print(f"  ... and {len(other) - 3} more")

    if total == 0:
        print("\nClean build — no assembly-class findings.")
        return 0
    print(f"\n{total} total finding(s).")
    return 1


def cmd_teardown(fork_path: Path) -> int:
    """Remove the smoke worktree and delete the throwaway branch."""
    if not _SMOKE_WORKTREE.is_dir():
        logger.info("worktree not found at %s — nothing to tear down", _SMOKE_WORKTREE)
        return 0
    _teardown_worktree(fork_path, _SMOKE_WORKTREE, _SMOKE_BRANCH)
    print(f"Torn down: {_SMOKE_WORKTREE}; branch {_SMOKE_BRANCH} deleted.")
    return 0


