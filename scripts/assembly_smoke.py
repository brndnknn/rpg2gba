def _get_first_map_const(fork_path: Path) -> str:
    """Return the first MAP_* constant name from the fork's map_groups.h."""
    h = fork_path / "include" / "constants" / "map_groups.h"
    text = h.read_text(encoding="utf-8")
    m = re.search(r"\b(MAP_[A-Z][A-Z0-9_]+)\s*=", text)
    if not m:
        raise RuntimeError(f"No MAP_* constant found in {h}")
    return m.group(1)


def _load_fork_trainer_consts(fork_path: Path) -> set[str]:
    """Return all TRAINER_* names defined in the fork's include/constants/ headers."""
    result: set[str] = set()
    for h in (fork_path / "include" / "constants").glob("*.h"):
        try:
            text = h.read_text(encoding="utf-8")
        except OSError:
            continue
        result.update(re.findall(r"#define\s+(TRAINER_[A-Z0-9_]+)", text))
    return result


def _load_trainers_id_map(out_dir: Path) -> dict[str, int]:
    """Load TRAINER_* constant → integer array-index id from trainers.json.

    The ``id`` field in trainers.json is the Marshal array index (the position
    of the trainer in trainers.dat), which is the integer the engine uses to
    identify a trainer battle.  Matches the key suffix in the constant name,
    e.g. ``TRAINER_ABE_GAYLE_166`` → ``id: 166``.
    """
    path = out_dir / "intermediate" / "trainers.json"
    if not path.is_file():
        raise FileNotFoundError(
            f"trainers.json not found at {path} — "
            "run `python -m rpg2gba.pipeline phase2` first"
        )
    data = json.loads(path.read_text(encoding="utf-8"))
    trainers: dict = data.get("trainers", {})
    return {key: int(val["id"]) for key, val in trainers.items()}


def _load_all_fork_constants(fork_path: Path) -> set[str]:
    """Build the set of all ALL_CAPS identifiers visible from event_scripts.s.

    Scans ``include/constants/*.h`` (``#define``-d flag/item/map/trainer
    constants) and ``asm/macros/*.inc`` (GAS equates like ``MSGBOX_NPC = 2``
    and ``MSGBOX_YESNO = 5`` from event.inc).  The union covers every constant
    the fork's assembler can resolve without our custom headers.
    """
    result: set[str] = set()
    for scan_dir in (
        fork_path / "include" / "constants",
        fork_path / "asm" / "macros",
    ):
        for suffix in ("*.h", "*.inc"):
            for f in scan_dir.glob(suffix):
                try:
                    result.update(_ALLCAPS_RE.findall(f.read_text(encoding="utf-8")))
                except OSError:
                    pass
    return result


def _read_header_constants(header_path: Path) -> set[str]:
    """Extract ALL_CAPS identifiers from a generated header file."""
    if not header_path.is_file():
        return set()
    return set(_ALLCAPS_RE.findall(header_path.read_text(encoding="utf-8")))


def _compile_pory_to_inc(pory_path: Path, inc_path: Path) -> tuple[bool, str]:
    """Invoke poryscript to compile *pory_path* → *inc_path*.

    Returns ``(ok, stderr)``.  Raises ``RuntimeError`` if the binary is absent.
    """
    from rpg2gba.conversion_agent.poryscript import (
        PoryscriptUnavailable,
        _config_args,
        binary_path,
    )

    try:
        binary = binary_path()
    except PoryscriptUnavailable as exc:
        raise RuntimeError(str(exc)) from exc

    inc_path.parent.mkdir(parents=True, exist_ok=True)
    proc = subprocess.run(
        [str(binary), "-i", str(pory_path), "-o", str(inc_path), *_config_args(binary)],
        capture_output=True,
        text=True,
    )
    return proc.returncode == 0, proc.stderr


def _create_or_reuse_worktree(
    fork_path: Path,
    worktree_path: Path,
    branch: str,
) -> None:
    """Create a git worktree on *branch* in *worktree_path*, or reuse if it exists.

    NEVER touches the fork's main checkout working tree.
    """
    if worktree_path.is_dir():
        logger.info("reusing existing worktree at %s", worktree_path)
        return

    worktree_path.parent.mkdir(parents=True, exist_ok=True)

    # Does the throwaway branch already exist in the fork?
    r = subprocess.run(
        ["git", "-C", str(fork_path), "rev-parse", "--verify", branch],
        capture_output=True,
        text=True,
    )
    if r.returncode == 0:
        # Branch exists — attach the new worktree to it
        cmd = [
            "git", "-C", str(fork_path),
            "worktree", "add", str(worktree_path), branch,
        ]
    else:
        # Branch absent — create it from fork HEAD
        cmd = [
            "git", "-C", str(fork_path),
            "worktree", "add", "-b", branch, str(worktree_path),
        ]
    subprocess.run(cmd, check=True)
    logger.info("created worktree at %s on branch %s", worktree_path, branch)


def _teardown_worktree(
    fork_path: Path,
    worktree_path: Path,
    branch: str,
) -> None:
    """Remove the git worktree and delete the throwaway branch."""
    subprocess.run(
        ["git", "-C", str(fork_path), "worktree", "remove", "--force", str(worktree_path)],
        check=True,
    )
    subprocess.run(
        ["git", "-C", str(fork_path), "branch", "-D", branch],
        check=True,
    )
    logger.info("worktree removed and branch %s deleted", branch)


