def main() -> int:
    ap = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    ap.add_argument(
        "--prepare-only",
        action="store_true",
        help=(
            "Stage the worktree (compile + headers + wiring) without running the build. "
            "Use this to set everything up at a convenient time and run `build` later "
            "when authorised."
        ),
    )
    ap.add_argument(
        "--define-unresolved",
        action="store_true",
        help=(
            "During prepare, dummy-define (to 0, smoke worktree only) any constant "
            "the fail-loud check finds unresolved, instead of aborting. Each token is "
            "logged as a FINDING. Use only after the families have been recorded."
        ),
    )
    ap.add_argument(
        "-v",
        "--verbose",
        action="store_true",
        help="Enable DEBUG-level logging.",
    )

    sub = ap.add_subparsers(dest="cmd")
    sub.add_parser(
        "prepare",
        help="Compile .pory→.inc, generate support headers, wire includes.",
    )
    sub.add_parser(
        "build",
        help="Run make -j<nproc> modern in the smoke worktree (takes minutes).",
    )
    sub.add_parser(
        "report",
        help="Parse smoke-build.log and print error clusters.",
    )
    sub.add_parser(
        "teardown",
        help="Remove the smoke worktree and delete the throwaway branch.",
    )

    args = ap.parse_args()
    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.INFO,
        format="%(asctime)s %(levelname)s %(name)s: %(message)s",
    )

    pipeline._load_dotenv()

    fork_str = os.environ.get("RPG2GBA_POKEEMERALD")
    if not fork_str:
        print(
            "ERROR: RPG2GBA_POKEEMERALD is not set "
            "(add it to .env-paths or your shell environment)",
            file=sys.stderr,
        )
        return 1
    fork_path = Path(fork_str)
    if not fork_path.is_dir():
        print(
            f"ERROR: RPG2GBA_POKEEMERALD={fork_path} is not a directory",
            file=sys.stderr,
        )
        return 1

    _, out_dir_raw = pipeline._resolve_paths()
    out_dir = Path(out_dir_raw)

    if args.cmd == "prepare":
        rc = cmd_prepare(fork_path, out_dir, define_unresolved=args.define_unresolved)
        if rc == 0:
            print(
                "\nWorktree staged. Run the build when authorised:\n"
                "  python scripts/assembly_smoke.py build"
            )
        return rc

    if args.cmd == "build":
        return cmd_build(out_dir)

    if args.cmd == "report":
        return cmd_report()

    if args.cmd == "teardown":
        return cmd_teardown(fork_path)

    # No subcommand given
    if args.prepare_only:
        rc = cmd_prepare(fork_path, out_dir, define_unresolved=args.define_unresolved)
        if rc == 0:
            print(
                "\nWorktree staged. Run the build when authorised:\n"
                "  python scripts/assembly_smoke.py build"
            )
        return rc

    # Default pipeline: prepare → build → report
    rc = cmd_prepare(fork_path, out_dir, define_unresolved=args.define_unresolved)
    if rc != 0:
        return rc
    rc_build = cmd_build(out_dir)
    rc_report = cmd_report()
    return 0 if (rc_build == 0 and rc_report == 0) else 1


if __name__ == "__main__":
    sys.exit(main())
